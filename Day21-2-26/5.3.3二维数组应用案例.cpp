#include <iostream>
using namespace std;
int main()
{
	//考试成绩统计：

	//案例描述：有三名同学（张三，李四，王五），在一次考试中的成绩分别如下表，请分别输出三名同学的总成绩

	//1、创建二维数组
	int scores[3][3] =
	{
		{100,100,100},
		{90,50,100},
		{60,70,80}
	};

	string name[3] = { "张三","李四" ,"王五" };
	//2、统计每个人的总和分数
	for (int i = 0; i < 3; i++)
	{
		int sum = 0; //统计分数总和变量
		for (int j = 0; j < 3; j++)
		{
			sum += scores[i][j];
			//cout << scores[i][j] << " ";
		}
		cout << name[i] << "的总分为：" << sum << endl;;
	}
	
	return 0;
}